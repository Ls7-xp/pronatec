a = 25
b = 10
print(a == b) #(25 não é igual a 10) 
print(a != b) #(25 é diferente de 10) 
print(a > b)  #(25 é maior do que 10)
print(a < b)  #(25 não é menor do que 10)
print(a >= b) #(25 é maior ou    igual a 10)
print(a <= b) #(25 não é menor ou igual a 10)

x = True
y = False
print(x and y)  # False (as duas precisam ser verdadeiras)
print(x or y)   # True (basta uma ser verdadeira)
print(not x)    # False (inverte o valor de x)

