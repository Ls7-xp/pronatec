def calcular_operacoes():
    valor1 = int(input("Digite o primeiro valor"))
    valor2 = int(input("Digite o segundo valor"))
    valor3 = int(input("Digite o terceiro valor"))
    valor4 = int(input("Digite o quarto valor"))
    multiplicação = valor1 * valor3
    soma = valor2 + valor4
    print(f"O resultado da multiplicação do primeiro pelo terceiro é: {multiplicação}.")
    print(f"O resultado da soma do segundo com o quarto é: {soma}.")
calcular_operacoes()